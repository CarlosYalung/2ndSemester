    public void loadUserInfo(){

    int currentUserId = Config.config.loggedInAID;

    try{
        Connection conn = Config.config.connectDB();

        String sql = "SELECT name, gmail FROM tble_user WHERE register_id = ?";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setInt(1, currentUserId);

        ResultSet rs = pst.executeQuery();

        if(rs.next()){
            Cname.setText(rs.getString("name"));
            Cemail.setText(rs.getString("gmail"));
        }

        rs.close();
        pst.close();
        conn.close();

    }catch(Exception e){
        JOptionPane.showMessageDialog(this, "Error loading user info: " + e.getMessage());
    }
}

