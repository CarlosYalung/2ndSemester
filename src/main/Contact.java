    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
      int currentUserId = Config.config.loggedInAID;

if(currentUserId == 0) {
    JOptionPane.showMessageDialog(this, "Please login first!");
    return;
}

String itemId = Citem.getText().trim();
String subject = jComboBox2.getSelectedItem().toString();

// Basic check
if(itemId.isEmpty()){
    JOptionPane.showMessageDialog(this, "Please enter an Item ID!");
    return;
}

try (Connection conn = Config.config.connectDB()) {

    String name = "";
    String email = "";

    String getUserSql = "SELECT name, gmail FROM tble_user WHERE register_id = ?";

    try(PreparedStatement userPst = conn.prepareStatement(getUserSql)){
        userPst.setInt(1, currentUserId);
        ResultSet rsUser = userPst.executeQuery();

        if(rsUser.next()){
            name = rsUser.getString("name");
            email = rsUser.getString("gmail");
        }
    }

    // CHECK ITEM STATUS
    String checkItemSql = "SELECT status FROM tble_buyer WHERE id_generate = ?";
    
    try (PreparedStatement pstmt = conn.prepareStatement(checkItemSql)) {

        pstmt.setString(1, itemId);
        ResultSet rs = pstmt.executeQuery();

        if(rs.next()) {

            String currentStatus = rs.getString("status");

            if("Refunded".equalsIgnoreCase(currentStatus)) {
                JOptionPane.showMessageDialog(this, "This item has already been refunded!");
                Citem.setText("");
                return;
            }

        } else {
            JOptionPane.showMessageDialog(this, "Invalid Item ID!");
            return;
        }
    }

    // INSERT MESSAGE
    String insertSql = "INSERT INTO tble_ContactUs (Cname, Cemail, Citem, Csub, Cmessage, date_submitted, status) "
                     + "VALUES (?, ?, ?, ?, ?, datetime('now'), 'Pending')";

    try (PreparedStatement insertPst = conn.prepareStatement(insertSql)) {

        insertPst.setString(1, name);   
        insertPst.setString(2, email);  
        insertPst.setString(3, itemId);
        insertPst.setString(4, subject);
        insertPst.setString(5, Cmessage.getText().trim());

        insertPst.executeUpdate();

      JOptionPane.showMessageDialog(this, "Message sent successfully!\nPlease wait for the admin to review and approve your request.");
        Citem.setText("");
        Cmessage.setText("");
    }

} catch(SQLException ex) {
    JOptionPane.showMessageDialog(this, "Error: " + ex.getMessage());
}
    }//GEN-LAST:event_jLabel3MouseClicked

